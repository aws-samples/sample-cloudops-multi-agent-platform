def handle_list_cost_allocation_tag_status(event: dict[str, Any]) -> dict[str, Any]:
    """Cost Explorer list_cost_allocation_tags — payer-only API.

    Compliance does NOT imply billing activation. This surfaces which tags
    are actually usable in Cost Explorer / CUR.
    """
    params: dict[str, Any] = {"MaxResults": 100}
    if event.get("status"):
        params["Status"] = event["status"]
    if event.get("type"):
        params["Type"] = event["type"]

    client = _client("ce", _CE_API_REGION)
    tags: list[dict[str, Any]] = []
    try:
        while True:
            resp = client.list_cost_allocation_tags(**params)
            for t in resp.get("CostAllocationTags", []):
                tags.append({
                    "tag_key": t.get("TagKey", ""),
                    "status": t.get("Status", ""),
                    "type": t.get("Type", ""),
                    "last_used_date": str(t.get("LastUsedDate", "")),
                })
            token = resp.get("NextToken") or ""
            if not token:
                break
            params["NextToken"] = token
    except ClientError as exc:
        if "AccessDenied" in str(exc):
            return {
                "error": (
                    "list_cost_allocation_tags is a payer-only API. Run "
                    "this tool from the management (payer) account."
                ),
            }
        return {"error": str(exc)}

    active = sum(1 for t in tags if t["status"] == "Active")
    return {
        "count": len(tags),
        "active_count": active,
        "inactive_count": len(tags) - active,
        "tags": tags,
    }


