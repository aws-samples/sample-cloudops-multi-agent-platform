def handle_find_untagged_resources(event: dict[str, Any]) -> dict[str, Any]:
    """Resource Explorer ``tag:none`` query — zero-tagged resources.

    Paginates up to min(limit, 1000) — Resource Explorer Search's server-side
    ceiling. Single resource_type and single region filters are pushed into
    the query server-side so the cap is less likely to bite.
    """
    account_ids = event.get("account_ids")
    regions = event.get("regions")
    resource_types = event.get("resource_types")
    limit = min(int(event.get("limit", _RE_SEARCH_HARD_CAP)), _RE_SEARCH_HARD_CAP)

    if isinstance(account_ids, str):
        account_ids = [account_ids]
    if isinstance(regions, str):
        regions = [regions]
    if isinstance(resource_types, str):
        resource_types = [resource_types]

    agg_region = _re_aggregator_region()
    if agg_region is None:
        return {
            "status": "resource_explorer_not_indexed",
            "message": (
                "Resource Explorer is not enabled in this account — "
                "enable an aggregator index to use this tool. Non-fatal."
            ),
        }

    # Push single-value filters into the query server-side. Multi-value
    # filters (and account_ids, which has no query syntax) fall back to
    # post-filter after pagination.
    query_parts = ["tag:none"]
    if resource_types and len(resource_types) == 1:
        query_parts.append(f"resourcetype:{resource_types[0]}")
    if regions and len(regions) == 1:
        query_parts.append(f"region:{_normalise_region(regions[0])}")

    try:
        raw, api_limit_reached = _re_search(
            " ".join(query_parts),
            client=_client("resource-explorer-2", agg_region),
            limit=limit,
        )
    except ClientError as exc:
        msg = str(exc)
        if "ValidationException" in msg:
            return {
                "status": "resource_explorer_not_indexed",
                "message": (
                    "Resource Explorer query failed — aggregator index may "
                    "not be enabled. Non-fatal."
                ),
            }
        return {"error": msg}

    # Post-filter for multi-value filters and account_ids.
    results: list[dict[str, Any]] = []
    for r in raw:
        owning_acct = r.get("OwningAccountId", "")
        r_region = r.get("Region", "")
        r_type = r.get("ResourceType", "")
        if account_ids and owning_acct not in account_ids:
            continue
        if regions and r_region not in regions:
            continue
        if resource_types and r_type not in resource_types:
            continue
        results.append({
            "resource_arn": r.get("Arn", ""),
            "account_id": owning_acct,
            "region": r_region,
            "resource_type": r_type,
            "service": r.get("Service", ""),
        })

    out: dict[str, Any] = {
        "status": "ok",
        "count": len(results),
        "raw_count": len(raw),
        "resources": results,
        "filter_applied": {
            "account_ids": account_ids or [],
            "regions": regions or [],
            "resource_types": resource_types or [],
        },
    }
    if api_limit_reached:
        out["api_limit_reached"] = True
        out["api_limit_note"] = (
            "Resource Explorer Search is capped at 1000 results per query "
            "(AWS-side limit, not paginatable). This scan hit that ceiling, "
            "so there may be more untagged resources not shown. Narrow the "
            "scope by passing a single resource_type or region so the server "
            "can filter before hitting the cap."
        )
    return out


