"""
Tag Governance MCP Tool — Lambda Implementation for AgentCore Gateway

Seven tools:
- get_required_tags                 Resolve required-tag policy from caller
                                    input or AWS Organizations Tag Policy.
- list_tag_keys_in_use              Enumerate tag keys actually applied to
                                    resources — starting point for teams
                                    without a formal policy.
- check_tag_compliance              Scan resources, classify compliance.
                                    Default: Resource Explorer + in-Python
                                    classifier. Opt-in: AWS server-side
                                    evaluation via GetResources.
- get_org_tag_compliance_summary    Aggregated counts via GetComplianceSummary.
                                    Cheap rollup — requires an attached
                                    AWS Tag Policy.
- find_untagged_resources           Resource Explorer tag:none query — zero-
                                    tagged resources only (GetResources
                                    cannot return them).
- list_cost_allocation_tag_status   Cost Explorer list_cost_allocation_tags.
                                    Compliance ≠ billing activation. Payer-
                                    only API.
- get_remediation_guidance          Resource Explorer deep-links pre-filtered
                                    to each violation bucket for bulk-fix
                                    via Actions > Manage tags.

Design notes:
- Single-account scope. No cross-account role assumption.
- Resource Explorer is the preferred inventory source. No silent fallback
  to the tagging API (which can't return zero-tagged resources).
- Caller-supplied policy is the primary input; AWS Tag Policy is a fallback
  for get_required_tags. AWS-evaluated mode requires an attached policy
  and pre-flights DescribeEffectivePolicy to avoid false-clean results.
- No persistence — policies resolve per-call.

Required IAM Permissions:
- tag:GetResources, tag:GetTagKeys, tag:GetTagValues
- tag:GetComplianceSummary
- ce:ListCostAllocationTags
- organizations:DescribeEffectivePolicy
- resource-explorer-2:ListIndexes, resource-explorer-2:Search
- sts:GetCallerIdentity
"""

