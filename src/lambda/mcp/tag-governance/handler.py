def _re_aggregator_region() -> str | None:
    """Return the Resource Explorer aggregator index region, or None."""
    try:
        client = _client("resource-explorer-2", "us-east-1")
        indexes: list[dict[str, Any]] = []
        for page in client.get_paginator("list_indexes").paginate():
            indexes.extend(page.get("Indexes", []))
    except Exception as exc:  # noqa: BLE001 — any failure means unusable
        logger.warning("Resource Explorer ListIndexes failed: %s", exc)
        return None
    agg = next((i for i in indexes if i.get("Type") == "AGGREGATOR"), None)
    return agg["Region"] if agg else None


def _re_search(
    query_string: str,
    *,
    client: Any,
    limit: int = _RE_SEARCH_HARD_CAP,
) -> tuple[list[dict[str, Any]], bool]:
    """Paginate a Resource Explorer search up to `limit` results.

    Returns (raw_results, api_limit_reached). api_limit_reached is True when
    the server-side 1000-per-query cap is hit, indicating results are
    almost certainly truncated.
    """
    results: list[dict[str, Any]] = []
    cap = min(limit, _RE_SEARCH_HARD_CAP)
    next_token: str | None = None
    while len(results) < cap:
        kwargs: dict[str, Any] = {
            "QueryString": query_string,
            "MaxResults": min(1000, cap - len(results)),
        }
        if next_token:
            kwargs["NextToken"] = next_token
        resp = client.search(**kwargs)
        results.extend(resp.get("Resources", []))
        next_token = resp.get("NextToken")
        if not next_token:
            break
    return results, len(results) >= _RE_SEARCH_HARD_CAP


def _normalise_region(region: str) -> str:
    """Normalise any casing of 'global' / blank to Resource Explorer's 'Global'."""
    return "Global" if not region or region.lower() == "global" else region


