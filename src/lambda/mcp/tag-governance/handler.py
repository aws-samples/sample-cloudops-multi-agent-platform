def handle_get_org_tag_compliance_summary(event: dict[str, Any]) -> dict[str, Any]:
    """Aggregated compliance counts via GetComplianceSummary.

    Returns counts only (no ARNs, no tags). Requires an attached AWS Tag
    Policy; surfaces a clear error otherwise.
    """
    group_by = event.get("group_by") or ["TARGET_ID"]
    if isinstance(group_by, str):
        group_by = [group_by]

    valid = {"TARGET_ID", "REGION", "RESOURCE_TYPE"}
    invalid = [g for g in group_by if g not in valid]
    if invalid:
        return {"error": f"invalid group_by values: {invalid}. Allowed: {sorted(valid)}"}

    params: dict[str, Any] = {"GroupBy": group_by, "MaxResults": 100}
    for src_key, dst_key in (
        ("account_ids", "TargetIdFilters"),
        ("regions", "RegionFilters"),
        ("resource_types", "ResourceTypeFilters"),
        ("tag_keys", "TagKeyFilters"),
    ):
        val = event.get(src_key)
        if val:
            params[dst_key] = val if isinstance(val, list) else [val]

    client = _client("resourcegroupstaggingapi", _TAG_API_REGION)

    summary_items: list[dict[str, Any]] = []
    try:
        while True:
            resp = client.get_compliance_summary(**params)
            summary_items.extend(resp.get("SummaryList", []))
            token = resp.get("PaginationToken") or ""
            if not token:
                break
            params["PaginationToken"] = token
    except ClientError as exc:
        translated = _translate_tag_policy_error(
            exc,
            mgmt_account_hint=(
                "GetComplianceSummary requires management-account access. "
                "Run this tool from the management (payer) account."
            ),
        )
        if translated is not None:
            return translated
        return {"error": str(exc)}

    total_noncompliant = sum(
        int(it.get("NonCompliantResources", 0) or 0) for it in summary_items
    )
    groups_fully_compliant = sum(
        1 for it in summary_items if int(it.get("NonCompliantResources", 0) or 0) == 0
    )
    return {
        "group_by": group_by,
        "count": len(summary_items),
        "total_noncompliant_resources": total_noncompliant,
        "groups_fully_compliant": groups_fully_compliant,
        "summary": summary_items,
        "region_queried": _TAG_API_REGION,
        "accuracy_caveat": (
            "These numbers are only as complete as your AWS Organizations "
            "Tag Policy configuration. GetComplianceSummary evaluates "
            "ONLY resources within the scope of attached tag policies — "
            "if the policy in the management account does not cover all "
            "required tags, all resource types, and does not use the "
            "enforced_for reporting clause, real non-compliance will be "
            "undercounted or invisible. For a definitive picture, run "
            "check_tag_compliance with caller-supplied required_tags to "
            "scan every resource and classify against your full policy, "
            "or use find_untagged_resources to surface zero-tagged "
            "resources that policy gaps would hide."
        ),
    }


