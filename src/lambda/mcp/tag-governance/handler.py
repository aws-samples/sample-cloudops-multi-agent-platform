def handle_check_tag_compliance(event: dict[str, Any]) -> dict[str, Any]:
    """Scan resources and classify against the required-tag policy.

    Modes:
      * Default (in-Python):  Resource Explorer inventory + local classifier.
                              Requires a resolved policy.
      * use_aws_evaluation:   AWS server-side evaluation via GetResources.
                              Requires an attached AWS Tag Policy; caller-
                              supplied required_tags is ignored.
    """
    if event.get("use_aws_evaluation"):
        return _check_compliance_aws_evaluated(event)
    return _check_compliance_in_python(event)


def _check_compliance_in_python(event: dict[str, Any]) -> dict[str, Any]:
    """Resource Explorer inventory + in-Python classifier."""
    policy = _resolve_policy(event)
    if policy is None:
        return _NO_POLICY_FOUND_ERROR

    resource_types = event.get("resource_types") or []
    max_resources = min(int(event.get("max_resources", 25)), 1000)
    include_system_managed = bool(event.get("include_system_managed", False))

    agg_region = _re_aggregator_region()
    if agg_region is None:
        return {
            "status": "resource_explorer_not_indexed",
            "error": (
                "Resource Explorer is not configured with an aggregator index. "
                "Enable it to use the in-Python compliance scan, or pass "
                "use_aws_evaluation=true to evaluate against an attached "
                "AWS Tag Policy instead."
            ),
        }

    # Match-all query is EMPTY STRING. "*" is a free-text search and returns
    # an arbitrary partial slice.
    query = " ".join(f"resourcetype:{t}" for t in resource_types)
    try:
        raw, api_limit_reached = _re_search(
            query, client=_client("resource-explorer-2", agg_region)
        )
    except ClientError as exc:
        logger.error("Resource Explorer Search failed: %s", exc)
        return {
            "status": "resource_explorer_not_indexed",
            "error": f"Resource Explorer search failed: {exc}",
        }

    classifications = [_classify(_map_re_result(r), policy) for r in raw]

    return _assemble_compliance_response(
        classifications=classifications,
        policy=policy,
        max_resources=max_resources,
        scan_method="resource_explorer",
        include_system_managed=include_system_managed,
        api_limit_reached=api_limit_reached,
    )


def _check_compliance_aws_evaluated(event: dict[str, Any]) -> dict[str, Any]:
    """AWS server-side evaluation via GetResources(IncludeComplianceDetails).

    Pre-flights DescribeEffectivePolicy because GetResources silently returns
    zero non-compliant resources when no policy is attached — a false-clean
    result that's indistinguishable from real compliance.
    """
    if not _has_effective_tag_policy():
        return {
            "error": (
                "AWS server-side evaluation requires an attached AWS Tag "
                "Policy on the caller's account (directly or via the OU "
                "tree). No effective policy was found, so GetResources "
                "would silently return zero non-compliant resources and "
                "mislead you. Either attach a Tag Policy in AWS "
                "Organizations, or call without use_aws_evaluation to use "
                "caller-supplied required_tags and the in-Python classifier."
            ),
            "code": "NoTagPoliciesAttached",
        }

    regions = event.get("regions")
    if regions is None:
        regions = [_default_region()]
    elif isinstance(regions, str):
        regions = [regions]

    resource_types = event.get("resource_types") or []
    max_resources = min(int(event.get("max_resources", 25)), 1000)
    include_system_managed = bool(event.get("include_system_managed", False))

    classifications: list[dict[str, Any]] = []
    region_errors: list[dict[str, Any]] = []

    for region in regions:
        params: dict[str, Any] = {
            "IncludeComplianceDetails": True,
            "ExcludeCompliantResources": True,
            "ResourcesPerPage": 100,
        }
        if resource_types:
            params["ResourceTypeFilters"] = resource_types
        try:
            client = _client("resourcegroupstaggingapi", region)
            while True:
                resp = client.get_resources(**params)
                for r in resp.get("ResourceTagMappingList", []):
                    classifications.append(_map_aws_evaluated_result(r, region))
                token = resp.get("PaginationToken") or ""
                if not token:
                    break
                params["PaginationToken"] = token
        except ClientError as exc:
            # Per-region errors don't short-circuit. The preflight already
            # caught the "no policy anywhere" case; if a specific region
            # returns a tag-policy error here it's a region-local quirk.
            region_errors.append({"region": region, "error": str(exc)})

    return _assemble_compliance_response(
        classifications=classifications,
        policy=None,  # AWS-evaluated mode has no local policy object
        max_resources=max_resources,
        scan_method="aws_server_side_evaluation",
        include_system_managed=include_system_managed,
        regions_scanned=regions,
        region_errors=region_errors,
    )


