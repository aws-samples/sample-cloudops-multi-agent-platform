# Tool 7: get_remediation_guidance
# ---------------------------------------------------------------------------

def handle_get_remediation_guidance(event: dict[str, Any]) -> dict[str, Any]:
    """Generate Resource Explorer deep-links bucketed by violation scope.

    Each link pre-filters Resource Explorer to exactly one bucket of
    (region, resource_type, tag_key, violation_type). Operator workflow:
    open link, select resources, Actions > Manage tags.

    Links are returned sorted by bucket size (most resources first) so the
    caller can surface the highest-impact fixes first. Capped at
    ``max_links`` (default 20) with a truncation flag when exceeded —
    prevents flooding the agent's context with low-impact buckets.

    Input: prefers ``remediation_buckets`` (aggregated, complete across the
    full scan, small regardless of account size). Falls back to
    ``non_compliant_resources`` (per-resource, may be truncated by
    check_tag_compliance's max_resources cap) for backwards compatibility.

    Self-contained fallback: if NEITHER ``remediation_buckets`` NOR
    ``non_compliant_resources`` is supplied, this tool runs
    check_tag_compliance internally (passing the same event through, so
    required_tags / use_aws_evaluation / resource_types / regions all apply)
    and uses the buckets it produces. This lets a planner that can't thread a
    prior tool's JSON output back in (e.g. Amazon Quick) call
    get_remediation_guidance standalone. Mirrors assess_dx_resiliency, which
    fetches its own topology when none is passed.
    """
    scan_method = event.get("scan_method", "resource_explorer")
    max_links = max(1, int(event.get("max_links", 20)))

    # Self-contained fallback: no buckets AND no per-resource list → run the
    # compliance scan ourselves to regenerate buckets. The internal scan must
    # finish within the caller's invocation timeout; for normally-sized
    # accounts this is well under the limit.
    if not event.get("remediation_buckets") and not event.get("non_compliant_resources"):
        compliance = handle_check_tag_compliance(event)
        # Propagate non-success responses verbatim (no policy, RE not indexed,
        # payer-only errors) so the caller sees the actionable hint, not an
        # empty link list.
        if compliance.get("error") or compliance.get("status") not in (None, "ok"):
            return compliance
        event = {
            **event,
            "remediation_buckets": compliance.get("remediation_buckets", []),
            # Use the scan's actual method so the invalid_value caveat is correct.
            "scan_method": compliance.get("scan_method", scan_method),
        }
        scan_method = event["scan_method"]

    # Prefer pre-aggregated buckets — they reflect the full scan, not a
    # truncated sample. Fall back to reconstructing from non_compliant_resources
    # only if buckets aren't supplied (e.g. older agent prompts).
    buckets_input = event.get("remediation_buckets")
    if buckets_input:
        buckets = [
            (
                (
                    b.get("tag_key", ""),
                    b.get("violation_type", ""),
                    b.get("account_id", "") or "",
                    b.get("region", "") or "",
                    b.get("resource_type", "") or "",
                ),
                int(b.get("count", 0) or 0),
            )
            for b in buckets_input
            if int(b.get("count", 0) or 0) > 0
        ]
    else:
        non_compliant = event.get("non_compliant_resources", [])
        if not non_compliant:
            return {
                "summary": (
                    "All scanned resources are compliant. No remediation needed."
                ),
                "links": [],
            }
        buckets = [
            (
                (b["tag_key"], b["violation_type"], b["account_id"],
                 b["region"], b["resource_type"]),
                b["count"],
            )
            for b in _build_remediation_buckets(non_compliant)
        ]

    if not buckets:
        return {
            "summary": "All scanned resources are compliant. No remediation needed.",
            "links": [],
        }

    # Pre-sort defensively; buckets from _build_remediation_buckets are
    # already ordered, but caller-supplied remediation_buckets may not be.
    ordered = sorted(buckets, key=lambda kv: (-kv[1], kv[0]))
    total_buckets = len(ordered)
    shown = ordered[:max_links]

    links = [
        _build_re_remediation_link(
            tag_key=tk, violation_type=vt, account_id=aid,
            region=reg, resource_type=rt, count=count,
        )
        for (tk, vt, aid, reg, rt), count in shown
    ]

    total_missing = sum(
        c for (_tk, vt, _aid, _reg, _rt), c in ordered if vt == "missing_tag"
    )
    total_invalid = sum(
        c for (_tk, vt, _aid, _reg, _rt), c in ordered if vt == "invalid_value"
    )
    parts: list[str] = []
    if total_missing:
        parts.append(f"{total_missing} violation(s) for missing required tags")
    if total_invalid:
        parts.append(f"{total_invalid} violation(s) for invalid tag values")
    summary = (
        f"{', '.join(parts)}. Links are ordered by bucket size (most "
        f"resources first). Each link opens AWS Resource Explorer "
        f"pre-filtered to exactly one (region, resource_type, tag_key) "
        f"bucket. In the console: select the resources, then Actions > "
        f"Manage tags to add or correct the tag in bulk."
    )
    if scan_method == "aws_server_side_evaluation":
        summary += (
            " Note: scan used AWS server-side evaluation; expected_values are "
            "not returned by that API, so invalid_value links let you inspect "
            "the current values rather than pre-filtering by them."
        )

    result: dict[str, Any] = {
        "summary": summary,
        "links": links,
        "total_buckets": total_buckets,
    }
    if total_buckets > max_links:
        result["links_truncated"] = True
        result["links_dropped"] = total_buckets - max_links
        result["links_truncation_note"] = (
            f"Showing the top {max_links} highest-impact buckets out of "
            f"{total_buckets} total. Re-run with max_links=<N> to see more, "
            f"or narrow the scope by passing resource_types or regions to "
            f"the preceding check_tag_compliance call."
        )
    return result


def _build_re_remediation_link(
    *, tag_key: str, violation_type: str, account_id: str,
    region: str, resource_type: str, count: int,
) -> dict[str, Any]:
    """Compose one Resource Explorer deep-link for a violation bucket.

    Query filters (space-separated, AND):
      * missing_tag   : -tag.key:<tag_key>
      * invalid_value :  tag.key:<tag_key>
      * region        :  region:<region>      ("Global" for global services)
      * resource_type :  resourcetype:<type>  (e.g. iam:role, s3:bucket)
    """
    region_filter = _normalise_region(region)
    tag_filter = (
        f"-tag.key:{tag_key}" if violation_type == "missing_tag"
        else f"tag.key:{tag_key}"
    )
    query_parts = [tag_filter, f"region:{region_filter}"]
    if resource_type:
        query_parts.append(f"resourcetype:{resource_type}")
    # quote_plus matches the AWS console's own wire format (spaces as "+").
    encoded = urllib.parse.quote_plus(" ".join(query_parts))
    url = f"{RESOURCE_EXPLORER_BASE}?region=us-east-1#/search?query={encoded}"

    if violation_type == "missing_tag":
        description = (
            f"Add '{tag_key}' tag to {count} {resource_type} resource(s) in "
            f"{region_filter} (account {account_id}). Open link, select all, "
            f"Actions > Manage tags > Add new tag."
        )
    else:
        description = (
            f"Correct '{tag_key}' value on {count} {resource_type} resource(s) "
            f"in {region_filter} (account {account_id}). Open link, inspect "
            f"current values, select affected resources, Actions > Manage tags."
        )

    return {
        "kind": "resource_explorer",
        "url": url,
        "description": description,
        "tag_key": tag_key,
        "violation_type": violation_type,
        "account_id": account_id,
        "region": region_filter,
        "resource_type": resource_type,
        "resource_count": count,
    }


