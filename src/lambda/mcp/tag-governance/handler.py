# Resource Explorer result mapping
# ---------------------------------------------------------------------------

def _map_re_result(result: dict[str, Any]) -> dict[str, Any]:
    """Map a Resource Explorer search hit to our internal resource dict."""
    tags: dict[str, str] = {}
    for prop in result.get("Properties") or []:
        if prop.get("Name") == "tags":
            for tag in prop.get("Data") or []:
                tags[tag["Key"]] = tag.get("Value", "")
    return {
        "arn": result.get("Arn", ""),
        "account_id": result.get("OwningAccountId", ""),
        "region": result.get("Region", ""),
        "resource_type": result.get("ResourceType", ""),
        "tags": tags,
    }


def _map_aws_evaluated_result(result: dict[str, Any], region: str) -> dict[str, Any]:
    """Map a GetResources-with-compliance hit into a classification dict.

    AWS ComplianceDetails arrays:
      * NoncompliantKeys             — required tags that are missing
      * KeysWithNoncompliantValues   — required tags whose value isn't allowed
    """
    arn = result.get("ResourceARN", "")
    parts = arn.split(":")
    service = parts[2] if len(parts) > 2 else ""
    account_id = parts[4] if len(parts) > 4 else ""
    resource_part = parts[-1] if parts else ""
    resource_type = (
        f"{service}:{resource_part.split('/')[0]}" if "/" in resource_part else service
    )
    tags = {t["Key"]: t.get("Value", "") for t in result.get("Tags") or []}

    cd = result.get("ComplianceDetails") or {}
    violations: list[dict[str, Any]] = [
        {
            "tag_key": k,
            "violation_type": "missing_tag",
            "expected_values": None,
            "actual_value": tags.get(k),
        }
        for k in cd.get("NoncompliantKeys") or []
    ]
    violations.extend({
        "tag_key": k,
        "violation_type": "invalid_value",
        "expected_values": None,  # AWS does not return the allowed set here
        "actual_value": tags.get(k),
    } for k in cd.get("KeysWithNoncompliantValues") or [])

    return {
        "arn": arn,
        "account_id": account_id,
        "region": region,
        "resource_type": resource_type,
        "existing_tags": tags,
        "compliance_status": "non_compliant" if violations else "compliant",
        "violations": sorted(violations, key=lambda v: v["tag_key"]),
    }


# ---------------------------------------------------------------------------
# Classification (in-Python path)
# ---------------------------------------------------------------------------

def _classify(resource: dict[str, Any], policy: dict[str, Any]) -> dict[str, Any]:
    """Evaluate a resource against the policy. Returns classification dict."""
    violations: list[dict[str, Any]] = []
    for key in policy["required_tag_keys"]:
        actual = resource["tags"].get(key)
        allowed = policy["allowed_values"].get(key) or []
        case_sensitive = policy["case_sensitive"].get(key, False)

        if actual is None:
            violations.append({
                "tag_key": key,
                "violation_type": "missing_tag",
                "expected_values": allowed or None,
                "actual_value": None,
            })
            continue
        if not allowed:
            continue  # Any non-empty value is valid

        if case_sensitive:
            ok = actual in allowed
        else:
            ok = actual.lower() in {v.lower() for v in allowed}
        if not ok:
            violations.append({
                "tag_key": key,
                "violation_type": "invalid_value",
                "expected_values": allowed,
                "actual_value": actual,
            })

    return {
        "arn": resource["arn"],
        "account_id": resource["account_id"],
        "region": resource["region"],
        "resource_type": resource["resource_type"],
        "existing_tags": resource["tags"],
        "compliance_status": "compliant" if not violations else "non_compliant",
        "violations": sorted(violations, key=lambda v: v["tag_key"]),
    }


# ---------------------------------------------------------------------------
# Response assembly — one builder for both evaluation modes.
# ---------------------------------------------------------------------------

def _top_n_counts(values, n: int = 10) -> list[dict[str, Any]]:
    """Top-n most-frequent non-empty values as [{name, count}]."""
    counts: dict[str, int] = {}
    for v in values:
        if not v:
            continue
        counts[v] = counts.get(v, 0) + 1
    ordered = sorted(counts.items(), key=lambda kv: (-kv[1], kv[0]))
    return [{"name": name, "count": count} for name, count in ordered[:n]]


def _build_remediation_buckets(
    non_compliant: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    """Aggregate violations into (tag_key, violation_type, account_id, region,
    resource_type) buckets with counts, sorted by count descending.

    This is small (cardinality = distinct violation combos, typically a few
    dozen) regardless of how many non-compliant resources exist, so it
    survives max_resources truncation and lets remediation prioritise
    correctly.
    """
    counts: dict[tuple, int] = {}
    for c in non_compliant:
        account_id = c.get("account_id", "") or ""
        region = c.get("region", "") or ""
        resource_type = c.get("resource_type", "") or ""
        for v in c.get("violations", []):
            key = (
                v.get("tag_key", ""),
                v.get("violation_type", ""),
                account_id,
                region,
                resource_type,
            )
            counts[key] = counts.get(key, 0) + 1
    ordered = sorted(counts.items(), key=lambda kv: (-kv[1], kv[0]))
    return [
        {
            "tag_key": tk,
            "violation_type": vt,
            "account_id": aid,
            "region": reg,
            "resource_type": rt,
            "count": count,
        }
        for (tk, vt, aid, reg, rt), count in ordered
    ]


def _assemble_compliance_response(
    *,
    classifications: list[dict[str, Any]],
    policy: dict[str, Any] | None,
    max_resources: int,
    scan_method: str,
    include_system_managed: bool,
    api_limit_reached: bool = False,
    regions_scanned: list[str] | None = None,
    region_errors: list[dict[str, Any]] | None = None,
) -> dict[str, Any]:
    """Build the standard check_tag_compliance response from classifications.

    Handles the shared concerns both evaluation modes need:
      - system-managed filtering and accounting
      - optional de-duplication by ARN (Resource Explorer can double-list
        resources visible through multiple views)
      - counts, breakdowns, top-N offenders
      - detail-list truncation
      - api-limit / system-managed / global-resources / region-errors notes
    """
    # Filter system-managed types and record the count.
    system_managed_excluded = 0
    if not include_system_managed:
        kept: list[dict[str, Any]] = []
        for c in classifications:
            if c["resource_type"] in SYSTEM_MANAGED_RESOURCE_TYPES:
                system_managed_excluded += 1
                continue
            kept.append(c)
        classifications = kept

    # De-dupe by ARN (keeps first occurrence).
    seen: set[str] = set()
    deduped: list[dict[str, Any]] = []
    for c in classifications:
        if c["arn"] in seen:
            continue
        seen.add(c["arn"])
        deduped.append(c)
    deduped.sort(key=lambda c: (c["account_id"], c["region"], c["arn"]))

    non_compliant = [c for c in deduped if c["compliance_status"] == "non_compliant"]
    compliant_count = len(deduped) - len(non_compliant)

    by_violation_type = {"missing_tag": 0, "invalid_value": 0}
    # Seed per-tag breakdown from the policy when present so every required
    # key shows up (even the ones with zero violations).
    if policy is not None:
        by_required_tag: dict[str, dict[str, int]] = {
            k: {"missing": 0, "invalid": 0} for k in policy["required_tag_keys"]
        }
    else:
        by_required_tag = {}
    for c in non_compliant:
        for v in c["violations"]:
            by_violation_type[v["violation_type"]] += 1
            bucket = by_required_tag.setdefault(
                v["tag_key"], {"missing": 0, "invalid": 0}
            )
            bucket["missing" if v["violation_type"] == "missing_tag" else "invalid"] += 1

    truncated = len(non_compliant) > max_resources
    aws_evaluated = scan_method == "aws_server_side_evaluation"

    # Aggregate violation buckets from the FULL non_compliant list — NOT
    # from the truncated detail slice. Downstream tools (remediation) use
    # this to prioritise by true impact instead of whatever sample happened
    # to land in non_compliant_resources after truncation.
    remediation_buckets = _build_remediation_buckets(non_compliant)

    result: dict[str, Any] = {
        # AWS-evaluated mode only returns non-compliant resources; compliant
        # counts / compliance_pct aren't knowable from the response.
        "total_resources": None if aws_evaluated else len(deduped),
        "compliant_count": None if aws_evaluated else compliant_count,
        "non_compliant_count": len(non_compliant),
        "compliance_pct": (
            None if aws_evaluated
            else (round(compliant_count / len(deduped) * 100, 1) if deduped else 100.0)
        ),
        "by_violation_type": by_violation_type,
        "by_required_tag": by_required_tag,
        "top_non_compliant_resource_types":
            _top_n_counts(c["resource_type"] for c in non_compliant),
        "top_non_compliant_accounts":
            _top_n_counts(c["account_id"] for c in non_compliant),
        "top_non_compliant_regions":
            _top_n_counts(c["region"] for c in non_compliant),
        "non_compliant_resources": non_compliant[:max_resources],
        "remediation_buckets": remediation_buckets,
        "truncated": truncated,
        "scan_method": scan_method,
        "system_managed_excluded": system_managed_excluded,
    }
    if policy is not None:
        result["effective_policy"] = policy
    if regions_scanned is not None:
        result["regions_scanned"] = regions_scanned
    if region_errors:
        result["region_errors"] = region_errors
    if system_managed_excluded:
        result["system_managed_note"] = (
            f"Excluded {system_managed_excluded} system-managed resource(s) "
            f"(e.g. SageMaker trial components, default parameter groups, "
            f"AWS-managed SSM documents). Pass include_system_managed=true "
            f"to include them."
        )
    if api_limit_reached:
        result["api_limit_reached"] = True
        result["api_limit_note"] = (
            "Resource Explorer Search is capped at 1000 results per query "
            "(AWS-side limit, not paginatable). This scan hit that ceiling, "
            "so the results are almost certainly truncated. Re-run with "
            "resource_types filters (e.g. resource_types=['ec2:instance']) "
            "to narrow the scope and get complete data per type."
        )
    if aws_evaluated:
        result["global_resources_note"] = (
            "AWS server-side evaluation uses a regional API (GetResources). "
            "Global-service resources (IAM roles/users/policies, Route 53 "
            "hosted zones, CloudFront distributions, WAF global rules, etc.) "
            "are NOT included in this scan regardless of which regions you "
            "pass. To evaluate those, use the default in-Python mode, which "
            "queries Resource Explorer and sees global resources."
        )
    return result


# ---------------------------------------------------------------------------
# Tool dispatch
# ---------------------------------------------------------------------------

def handler(event, context):
    logger.info("Event: %s", json.dumps(event, default=str))
    extended_tool_name = context.client_context.custom["bedrockAgentCoreToolName"]
    tool_name = extended_tool_name.split("___")[1]
    logger.info("Tool: %s", tool_name)

    fn = _TOOL_HANDLERS.get(tool_name)
    if not fn:
        return {
            "error": f"Unknown tool: {tool_name}",
            "available_tools": list(_TOOL_HANDLERS.keys()),
        }
    try:
        response = fn(event or {})
        logger.info("Response: %s", json.dumps(response, default=str))
        return response
    except Exception as exc:  # noqa: BLE001 — surface the error to the agent
        logger.exception("Unhandled error in %s", tool_name)
        return {"error": f"{tool_name} failed: {exc}"}


# ---------------------------------------------------------------------------
# Tool 1: get_required_tags
# ---------------------------------------------------------------------------

def handle_get_required_tags(event: dict[str, Any]) -> dict[str, Any]:
    """Resolve required-tag policy from caller input or AWS Organizations."""
    policy = _resolve_policy(event)
    return policy if policy is not None else _NO_POLICY_FOUND_ERROR


# ---------------------------------------------------------------------------
# Tool 2: list_tag_keys_in_use
# ---------------------------------------------------------------------------

def handle_list_tag_keys_in_use(event: dict[str, Any]) -> dict[str, Any]:
    """Return every tag key in use in the caller's account + region."""
    region = event.get("region") or _default_region()
    client = _client("resourcegroupstaggingapi", region)

    keys: list[str] = []
    try:
        for page in client.get_paginator("get_tag_keys").paginate():
            keys.extend(page.get("TagKeys", []))
    except ClientError as exc:
        logger.error("get_tag_keys failed: %s", exc)
        return {"error": str(exc)}

    return {
        "count": len(set(keys)),
        "tag_keys": sorted(set(keys)),
        "region": region,
        "note": (
            "Keys returned are those in use in this account + region. "
            "Use this to seed a required_tags policy, then call "
            "check_tag_compliance with it."
        ),
    }


